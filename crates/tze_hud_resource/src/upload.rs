    // ─── Out-of-order chunk rejection ─────────────────────────────────────────

    #[tokio::test]
    async fn out_of_order_chunk_rejected() {
        let store = default_store();
        let agent = "order-agent";
        let uid = upload_id(1);
        let hash = [0xDD; 32];

        store
            .handle_upload_start(UploadStartRequest {
                agent_namespace: agent.into(),
                agent_capabilities: caps(),
                agent_budget: unlimited_budget(),
                upload_id: uid,
                resource_type: ResourceType::ImageRgba8,
                expected_hash: hash,
                total_size: 256 * 1024,
                inline_data: vec![],
            })
            .await
            .unwrap();

        // Chunk index 0 is fine.
        store
            .handle_upload_chunk(agent, uid, 0, vec![0u8; 64 * 1024])
            .await
            .unwrap();

        // Chunk index 2 (skipping 1) is out of order.
        let err = store
            .handle_upload_chunk(agent, uid, 2, vec![0u8; 64 * 1024])
            .await
            .unwrap_err();

        assert!(matches!(err, ResourceError::InvalidChunk(_)));
    }

