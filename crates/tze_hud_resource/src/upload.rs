    // ─── Chunked upload ───────────────────────────────────────────────────────

    // Acceptance: chunked upload 3-phase success (spec lines 62-64).
    #[tokio::test]
    async fn chunked_upload_success() {
        let store = default_store();
        let agent = "chunked-agent";
        let uid = upload_id(10);

        // Build 200 KiB of data (> 64 KiB inline limit).
        let chunk_size = 64 * 1024;
        let total_size = chunk_size * 4; // 4 × 64 KiB
        let raw_data: Vec<u8> = (0..total_size).map(|i| (i % 251) as u8).collect();
        let hash = *blake3::hash(&raw_data).as_bytes();

        // Start.
        let start_result = store
            .handle_upload_start(UploadStartRequest {
                agent_namespace: agent.into(),
                agent_capabilities: caps(),
                agent_budget: unlimited_budget(),
                upload_id: uid,
                resource_type: ResourceType::ImageRgba8,
                expected_hash: hash,
                total_size,
                inline_data: vec![],
            })
            .await
            .unwrap();

        // Chunked path returns None.
        assert!(start_result.is_none());

        // Send 4 chunks.
        for i in 0u32..4 {
            let offset = (i as usize) * chunk_size;
            let chunk = raw_data[offset..offset + chunk_size].to_vec();
            store
                .handle_upload_chunk(agent, uid, i, chunk)
                .await
                .unwrap();
        }

        // Complete.
        let result = store
            .handle_upload_complete(agent, uid, &caps(), &unlimited_budget())
            .await
            .unwrap();

        assert!(!result.was_deduplicated);
        assert_eq!(result.resource_id.as_bytes(), &hash);
    }

    // Acceptance: hash mismatch rejected (spec lines 66-68).
