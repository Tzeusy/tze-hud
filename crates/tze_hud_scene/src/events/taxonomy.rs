#[cfg(test)]
mod tests {
    use super::*;

    /// WHEN any event is generated THEN it must map to exactly one subscription
    /// category (spec lines 11-12).
    #[test]
    fn scene_tile_created_routes_to_scene_topology() {
        let cat = SubscriptionCategory::for_event_type("scene.tile.created");
        assert_eq!(cat, Some(SubscriptionCategory::SceneTopology));
    }

    #[test]
    fn scene_zone_occupancy_changed_routes_to_zone_events() {
        // ZoneOccupancyChanged appears in BOTH SceneTopology and ZoneEvents.
        // `for_event_type` returns ZoneEvents (most specific match).
        // Dual routing with deduplication is handled by the event bus (bead #2).
        let cat = SubscriptionCategory::for_event_type("scene.zone.occupancy_changed");
        assert_eq!(cat, Some(SubscriptionCategory::ZoneEvents));
    }

    #[test]
    fn scene_focus_changed_routes_to_focus_events() {
        let cat = SubscriptionCategory::for_event_type("scene.focus.changed");
        assert_eq!(cat, Some(SubscriptionCategory::FocusEvents));
    }

    #[test]
    fn system_degradation_changed_routes_to_degradation_notices() {
        let cat = SubscriptionCategory::for_event_type("system.degradation_changed");
        assert_eq!(cat, Some(SubscriptionCategory::DegradationNotices));
    }

    #[test]
    fn system_lease_revoked_routes_to_lease_changes() {
        let cat = SubscriptionCategory::for_event_type("system.lease_revoked");
        assert_eq!(cat, Some(SubscriptionCategory::LeaseChanges));
    }

    #[test]
    fn agent_event_routes_to_agent_events() {
        let cat =
            SubscriptionCategory::for_event_type("agent.doorbell_agent.doorbell.ring");
        assert_eq!(cat, Some(SubscriptionCategory::AgentEvents));
    }

    #[test]
    fn input_event_routes_to_input_events() {
        let cat = SubscriptionCategory::for_event_type("input.pointer.down");
        assert_eq!(cat, Some(SubscriptionCategory::InputEvents));
    }

    #[test]
    fn unknown_event_type_has_no_category() {
        let cat = SubscriptionCategory::for_event_type("unknown.event.type");
        assert_eq!(cat, None);
    }

    #[test]
    fn mandatory_categories_cannot_be_opted_out() {
        assert!(SubscriptionCategory::DegradationNotices.is_mandatory());
        assert!(SubscriptionCategory::LeaseChanges.is_mandatory());
    }

    #[test]
    fn non_mandatory_categories_are_optional() {
        assert!(!SubscriptionCategory::SceneTopology.is_mandatory());
        assert!(!SubscriptionCategory::AgentEvents.is_mandatory());
        assert!(!SubscriptionCategory::InputEvents.is_mandatory());
        assert!(!SubscriptionCategory::FocusEvents.is_mandatory());
        assert!(!SubscriptionCategory::ZoneEvents.is_mandatory());
        assert!(!SubscriptionCategory::TelemetryFrames.is_mandatory());
        assert!(!SubscriptionCategory::AttentionEvents.is_mandatory());
    }

    #[test]
    fn subscription_category_has_correct_top_level_category() {
        assert_eq!(
            SubscriptionCategory::SceneTopology.event_category(),
            EventCategory::Scene
        );
        assert_eq!(
            SubscriptionCategory::InputEvents.event_category(),
            EventCategory::Input
        );
        assert_eq!(
            SubscriptionCategory::DegradationNotices.event_category(),
            EventCategory::System
        );
        assert_eq!(
            SubscriptionCategory::LeaseChanges.event_category(),
            EventCategory::System
        );
        assert_eq!(
            SubscriptionCategory::AgentEvents.event_category(),
            EventCategory::Scene
        );
    }
}
