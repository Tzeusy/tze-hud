// â”€â”€â”€ Top-level category â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// The three top-level categories every event belongs to.
///
/// Spec: scene-events/spec.md lines 5-17, Requirement: Three-Category Event Taxonomy.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum EventCategory {
    /// Input events owned by RFC 0004 (pointer, keyboard, gesture, focus).
    /// Delivered only to the agent that owns the target tile.
    Input,
    /// Scene events generated by the compositor on scene-graph mutations.
    /// Delivered to agents subscribed to the appropriate scene category.
    Scene,
    /// System events generated by the runtime kernel (lease, degradation,
    /// telemetry, attention). Always delivered for mandatory categories.
    System,
}

// â”€â”€â”€ Subscription categories â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€

/// The nine subscription categories agents use to filter event delivery.
///
/// Spec: scene-events/spec.md lines 156-184, Requirement: Subscription Model
/// with Category Filtering.
///
/// Category-to-event-type-prefix mapping:
///
/// | Category              | Event type prefix pattern |
/// |-----------------------|--------------------------|
/// | `SceneTopology`       | `scene.*`                |
/// | `InputEvents`         | `input.*`                |
/// | `FocusEvents`         | `scene.focus.*`          |
/// | `DegradationNotices`  | `system.degradation_*`   |
/// | `LeaseChanges`        | `system.lease_*`         |
/// | `ZoneEvents`          | `scene.zone.*`           |
/// | `TelemetryFrames`     | `system.telemetry_*`     |
/// | `AttentionEvents`     | `system.attention_*`     |
/// | `AgentEvents`         | `agent.*`                |
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum SubscriptionCategory {
    /// All scene topology changes: tile create/delete, tab create, agent join.
    /// Prefix: `scene.*`. Maps to `EventCategory::Scene`.
    SceneTopology,
    /// Input events (RFC 0004): pointer, keyboard, gesture.
    /// Prefix: `input.*`. Maps to `EventCategory::Input`.
    InputEvents,
    /// Focus events: gained, lost.
    /// Prefix: `scene.focus.*`. Maps to `EventCategory::Scene`.
    FocusEvents,
    /// Degradation level notifications. **Cannot be opted out of.**
    /// Prefix: `system.degradation_*`. Maps to `EventCategory::System`.
    DegradationNotices,
    /// Lease lifecycle events. **Cannot be opted out of.**
    /// Prefix: `system.lease_*`. Maps to `EventCategory::System`.
    LeaseChanges,
    /// Zone state events: occupancy changes, publish results.
    /// Prefix: `scene.zone.*`. Maps to `EventCategory::Scene`.
    ZoneEvents,
    /// Telemetry frame snapshots.
    /// Prefix: `system.telemetry_*`. Maps to `EventCategory::System`.
    TelemetryFrames,
    /// Attention budget warnings and state.
    /// Prefix: `system.attention_*`. Maps to `EventCategory::System`.
    AttentionEvents,
    /// Agent-emitted events (prefixed with `agent.<namespace>.`).
    /// Prefix: `agent.*`. Maps to `EventCategory::Scene` (agent-observable).
    AgentEvents,
}

impl SubscriptionCategory {
    /// Whether agents can opt out of this category.
    ///
    /// `DegradationNotices` and `LeaseChanges` are mandatory; all others are
    /// optional and filtered by the agent's granted capabilities.
    pub fn is_mandatory(self) -> bool {
        matches!(
            self,
            SubscriptionCategory::DegradationNotices | SubscriptionCategory::LeaseChanges
        )
    }

    /// The event type prefix pattern that routes into this category.
    ///
    /// Trailing `*` matches any suffix with the same initial segment(s).
    pub fn event_type_prefix(self) -> &'static str {
        match self {
            SubscriptionCategory::SceneTopology => "scene.",
            SubscriptionCategory::InputEvents => "input.",
            SubscriptionCategory::FocusEvents => "scene.focus.",
            SubscriptionCategory::DegradationNotices => "system.degradation_",
            SubscriptionCategory::LeaseChanges => "system.lease_",
            SubscriptionCategory::ZoneEvents => "scene.zone.",
            SubscriptionCategory::TelemetryFrames => "system.telemetry_",
            SubscriptionCategory::AttentionEvents => "system.attention_",
            SubscriptionCategory::AgentEvents => "agent.",
        }
    }

    /// The top-level `EventCategory` this subscription category belongs to.
    pub fn event_category(self) -> EventCategory {
        match self {
            SubscriptionCategory::SceneTopology
            | SubscriptionCategory::FocusEvents
            | SubscriptionCategory::ZoneEvents
            | SubscriptionCategory::AgentEvents => EventCategory::Scene,
            SubscriptionCategory::InputEvents => EventCategory::Input,
            SubscriptionCategory::DegradationNotices
            | SubscriptionCategory::LeaseChanges
            | SubscriptionCategory::TelemetryFrames
            | SubscriptionCategory::AttentionEvents => EventCategory::System,
        }
    }

    /// Return the subscription category that delivers a given event type, if any.
    ///
    /// An event type that matches no subscription category is not routable.
    /// Prefer the most specific match: `scene.focus.*` before `scene.*`.
    pub fn for_event_type(event_type: &str) -> Option<Self> {
        // Test most-specific prefixes first.
        if event_type.starts_with("scene.focus.") {
            return Some(SubscriptionCategory::FocusEvents);
        }
        if event_type.starts_with("scene.zone.") {
            return Some(SubscriptionCategory::ZoneEvents);
        }
        if event_type.starts_with("scene.") {
            return Some(SubscriptionCategory::SceneTopology);
        }
        if event_type.starts_with("input.") {
            return Some(SubscriptionCategory::InputEvents);
        }
        if event_type.starts_with("system.degradation_") {
            return Some(SubscriptionCategory::DegradationNotices);
        }
        if event_type.starts_with("system.lease_") {
            return Some(SubscriptionCategory::LeaseChanges);
        }
        if event_type.starts_with("system.telemetry_") {
            return Some(SubscriptionCategory::TelemetryFrames);
        }
        if event_type.starts_with("system.attention_") {
            return Some(SubscriptionCategory::AttentionEvents);
        }
        if event_type.starts_with("agent.") {
            return Some(SubscriptionCategory::AgentEvents);
        }
        None
    }
}

